 .CSEG
.ORG $0000

;.include "libreria_tablas.inc"
;.include "libreria_subrutinas.inc"

.def regUsoGeneral = r16
.def regVecesPulsado = r29
.def regDesbloqueo = r19
.def regAdchVertical = r17
.def regAdchHorizontal = r24
.def regFila = r31


; Inicio del código

start:

	; Creación de la pila

	ldi regUsoGeneral,high(RAMEND)
	out SPH, regUsoGeneral		
	ldi regUsoGeneral, low(RAMEND)     
	out SPL, regUsoGeneral
		
	; Establecemos como entrada los pines 0 y 1 del Puerto A
	; El resto de pines serán de salida.
	
	ldi regUsoGeneral, 0b01111100  
	out DDRA, regUsoGeneral
			
	; Establecemos como salida los pines del puerto B
	; Refinarlo	
	
	ldi regUsoGeneral,255	
	out DDRB, regUsoGeneral

	; Establecemos como salida los pines del puerto C
	; a través de estos pines le enviaremos información a la pantalla

	ldi regUsoGeneral,255	
	out DDRC, regUsoGeneral
	
	; Establecemos como salida los pines del puerto D	

	ldi regUsoGeneral,255	
	out DDRD, regUsoGeneral

	; Cargamos la dirección del puntero con la tabla Blanco ya que 
	; limpiaremos la pantalla al iniciar el código

	ldi ZH,HIGH(Blanco*2)
	ldi ZL,LOW(Blanco*2)

	; Inicializamos los registros encargados de la comprobación del pin
	; y la comprobación de la cantidad de veces pulsada

	ldi regVecesPulsado, 0 
	ldi regDesbloqueo,0


programa:
	; Iniciamos la configuración de la pantalla para empezar a trabar con ella	

	sbi PORTD,7
	ldi regUsoGeneral, 0b00111111
	out PORTC, regUsoGeneral


	rcall SRPantallaIzquierdaOn
	rcall SRModoConfig
	rcall SRPantallaDerechaOn
	rcall SRModoConfig

	cbi PORTB,0
	cbi PORTB,1


	; A través del modo configuración establecemos que comenzaremos a 
	; dibujar la pantalla en el pixel 1 del eje horizontal

	ldi regUsoGeneral,0b01000000
	out PORTC, regUsoGeneral
	rcall SRModoConfig

	; A través del modo configuración establecemos que comenzaremos a 
	; dibujar la pantalla en el pixel 1 del eje vertical

	ldi regUsoGeneral,0b10111000
	out PORTC, regUsoGeneral
	rcall SRModoConfig


	;Imprimimos por pantalla el logo de la ULL
	
	ldi ZH,HIGH(ULL*2)     ;El puntero tendrá la dirección de la tabla para el menú 
	ldi ZL,LOW(ULL*2)
	rcall SRPintar
	rcall RetardoLargo

inicio:
	; Limpiamos la pantalla

	rcall SRLimpiar

	; Dibujamos los números

	rcall SRNumeros
	
	
bucle:
	; Configuramos admux para que nos lea el pin 3 del puerto A
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;	Continuar escribiendo

	ldi regUsoGeneral,0b01000001
	out admux,regUsoGeneral
	cbi PORTA,2
	sbi PORTA,3
	ldi regUsoGeneral, 0b10000000
	out adcsra, regUsoGeneral
	rjmp bucleConfig
		
bucleComprobacionVertical:
	
	; Creamos un bucle que compruebe constantemente el pin 4 del
	; convertidor adcsra ya que este se activa cuando acaba la conversión

	sbis adcsra,4
	rjmp bucleComprobacionVertical
	rcall Retardo4ms

	; Introducimos los valores de la conversión en los registros

	in regUsoGeneral, adcl
	in regAdchVertical, adch

	; Guardamos el valor del adcl en el r18 para más tarde verificar que
	; la medida ha sido correcta
	mov r18,regUsoGeneral

	; Volvemos a realizar la medición y su bucle

	sbi adcsra,6

bucleComprobacionVertical2:
	sbis adcsra,4
	rjmp bucleComprobacionVertical2
	in regUsoGeneral, adcl
	in regAdchVertical, adch	

	; Comparamos la medida anterior con la nueva, si no coincide
	; comenzamos de nuevo el proceso

	cp regUsoGeneral,r18
	brne bucleConfig

	; Realizamos un primer filtro a través de los valores de adch

	cpi regAdchVertical,1
	breq posibleFila23
	cpi regAdchVertical,2
	breq posibleFila12
	cpi regAdchVertical,3
	breq primeraFila	
	rjmp posibleFila3

; Bucle intermedio para reiniciar la medida

bucleConfig:

	ldi regUsoGeneral,0b01000001
	out admux,regUsoGeneral
	clr regAdchVertical
	cbi PORTA,2
	sbi PORTA,3
	sbi adcsra,6
	rjmp bucleComprobacionVertical
	
; Una vez comparado el valor de adch compararemos el valor de
; adcl para saber qué fila ha sido presionada y proceder a leer
; el eje vertical de la pulsación

posibleFila3:
	cpi regUsoGeneral, 143
	brlo bucleConfig
	rjmp terceraFila

posibleFila23:

	cpi regUsoGeneral,114
	brlo terceraFila 
	rjmp segundaFila

posibleFila12:

	cpi regUsoGeneral, 84 
	brlo segundaFila
	rjmp primeraFila

; Asignamos el valor correspondiente a cada fila al registro 
; encargado 
	
primeraFila:
	ldi regFila,1
	rjmp bucleHorizontal

segundaFila:
	ldi regFila,2
	rjmp bucleHorizontal

terceraFila:
	ldi regFila,3
	rjmp bucleHorizontal

;Leemos el eje horizontal de la pulsación a través del mismo proceso
; anteriormente hecho

bucleHorizontal:	

	ldi regUsoGeneral,0b01000000
	out admux,regUsoGeneral

	sbi PORTA,2
	cbi PORTA,3
	rcall Retardo4ms

	sbi adcsra,6

bucleComprobacionHorizontal:

	sbis adcsra,4
	rjmp bucleComprobacionHorizontal
	ldi r23,37

	rcall SRPantallaIzquierdaOn	
	in regUsoGeneral, adcl
	in regAdchHorizontal, adch

	; Una vez obtenido los valores de la conversión procedemos a filtrar por
	; el valor del adch

	cpi regAdchHorizontal,3
	breq terceraColumna 
	cpi regAdchHorizontal,2
	breq posibleColumna23
	cpi regAdchHorizontal,1
	breq posibleColumna12
	rjmp primeraColumna 

; Filtramos por los valores de adcl para saber qué columna ha sido seleccionada

posibleColumna12:
	cpi regUsoGeneral,105
	brlo primeraColumna 
	rjmp segundaColumna

posibleColumna23:
	cpi regUsoGeneral, 43
	brlo segundaColumna
	rjmp terceraColumna 

; Comparamos el valor del registro encargado de almacenar el numero
; de la fila y encendemos el número correspondiente
	
primeraColumna:
	
	; Comparación número 1

	cpi regFila, 1
	breq encenderNumero01

	; Comparación numero 4

	cpi regFila, 2
	breq encenderNumero04
	
	; Comparación numero 7
	cpi regFila, 3
	breq encenderNumero07
	
	ldi regFila,0
	rjmp inicio

;Acordarse de nombre el problema de las llamadas a las breq
; Parte del código encargadas para la llamada a las subrutinas de encendido del número
	encenderNumero01:
		rcall SREncenderNumero1
		rjmp inicio
	encenderNumero04:
		rcall SREncenderNumero4
		rjmp inicio
	encenderNumero07:
		rcall SREncenderNumero7
		rjmp inicio


segundaColumna:

	cpi regFila, 1
	breq encenderNumero02

	cpi regFila, 2
	breq encenderNumero05

	cpi regFila, 3
	breq encenderNumero08

	ldi regFila,0
	rjmp inicio

	encenderNumero02:
		rcall SREncenderNumero2
		rjmp inicio
	encenderNumero05:
		rcall SREncenderNumero5
		rjmp inicio
	encenderNumero08:
		rcall SREncenderNumero8
		rjmp inicio

terceraColumna:
	cpi regFila, 1
	breq encenderNumero03

	cpi regFila, 2
	breq encenderNumero06

	cpi regFila, 3
	breq encenderNumero09

	ldi regFila,0
	rjmp inicio

	encenderNumero03:
		rcall SREncenderNumero3
		rjmp inicio
	encenderNumero06:
		rcall SREncenderNumero6
		rjmp inicio
	encenderNumero09:
		rcall SREncenderNumero9
		rjmp inicio

	
; Subrutinas de encendido de número
SREncenderNumero1:
	
	ldi ZH,HIGH(Numero1*2)    
	ldi ZL,LOW(Numero1*2)
	rcall SRPintar
	
	; Llamada a la secuencia de desbloqueo3
	
	rcall SRDesbloqueo3

	; Llamada al contador
	rcall SRContador
	ret

SREncenderNumero2:

	ldi ZH, HIGH(Numero2*2)    
	ldi ZL, LOW(Numero2*2)
	rcall SRPintar
	rcall SRContador
	ret

SREncenderNumero3:

	ldi ZH,HIGH(Numero3*2)    
	ldi ZL,LOW(Numero3*2)
	rcall SRPintar
	rcall SRContador
	ret

SREncenderNumero4:

	ldi ZH,HIGH(Numero4*2)    
	ldi ZL,LOW(Numero4*2)
	rcall SRPintar
	rcall SRDesbloqueo1
	rcall SRContador
	ret

SREncenderNumero5:
	ldi ZH,HIGH(Numero5*2)    
	ldi ZL,LOW(Numero5*2)
	rcall SRPintar
	rcall SRDesbloqueo
	rcall SRContador
	ret

SREncenderNumero6:
	ldi ZH,HIGH(Numero6*2)    
	ldi ZL,LOW(Numero6*2)
	rcall SRPintar
	rcall SRContador
	ret

SREncenderNumero7:
	ldi ZH,HIGH(Numero7*2)    
	ldi ZL,LOW(Numero7*2)
	rcall SRDesbloqueo2
	rcall SRPintar
	rcall SRContador
	ret

SREncenderNumero8:
	ldi ZH,HIGH(Numero8*2)    
	ldi ZL,LOW(Numero8*2)
	rcall SRPintar
	rcall SRContador
	ret

SREncenderNumero9:
	ldi ZH,HIGH(Numero9*2)    
	ldi ZL,LOW(Numero9*2)
	rcall SRPintar
	rcall SRContador
	ret

; Subrutina encargada del desbloqueo, se irán llamando a las secuencias en orden.
; estas comparan el registro encargado y en caso de estar con el valor correcto
; se aumentará el valor y saldrá de la subrutina, de lo contrario saldrá sin realizar 
; ningún cambio
SRDesbloqueo:
	cpi regDesbloqueo,0
	breq SRDesbloqueoAumento
	ret

SRDesbloqueo1:
	cpi regDesbloqueo,1
	breq SRDesbloqueoAumento
	ret

SRDesbloqueo2:
	cpi regDesbloqueo,2
	breq SRDesbloqueoAumento
	ret
SRDesbloqueo3:
	cpi regDesbloqueo,3
	breq SRDesbloqueoAumento
	ret

SRDesbloqueoAumento:
	inc regDesbloqueo
	ret

; Subrutina encargada de leer el número de veces que se ha pulsado
; un número en la pantalla. 

SRContador:
	inc regVecesPulsado

	; Comprueba el valor del registro de desbloqueo, si es correcto
	; llama a la etiqueta desbloquear.

	cpi regDesbloqueo,4
	breq SRDesbloquear

	; Comprueba el valor del registro de pulsaciones, en caso de haber
	; pulsado 4 veces saltará a la etiqueta Fallo

	cpi regVecesPulsado,4
	breq SRFallo
	ret

; Subrutina de desbloqueo

SRDesbloquear:

	; Pinta en la GLCD la imagen de desbloqueo

	rcall SRDesbloqueado
	rcall RetardoLargo

	; Setea los valores de las cuentas a 0

	ldi regDesbloqueo,0
	ldi regVecesPulsado,0
	ret

; Subrutina de fallo

SRFallo:
	; Pinta en la GLCD la imagen de failed

	rcall Fallado
	rcall RetardoLargo
	
	; Setea los valores de las cuentas a 0

	ldi regDesbloqueo,0
	ldi regVecesPulsado,0
	ret

;Comentar aquí

SRNumeros:
	ldi ZH,HIGH(Numeros*2)
	ldi ZL,LOW(Numeros*2)
	rcall SRPintar
	ret

SRLimpiar:
	
	ldi ZH,HIGH(Blanco*2)     ;El puntero tendrá la dirección de la tabla para el menú 
	ldi ZL,LOW(Blanco*2)
	rcall SRPintar
	ret

Fallado:
	ldi ZH,HIGH(Failed*2)     ;El puntero tendrá la dirección de la tabla para el menú 
	ldi ZL,LOW(Failed*2)
	rcall SRPintar
	ret

SRDesbloqueado:
	
	ldi ZH,HIGH(Unlocked*2)     ;El puntero tendrá la dirección de la tabla para el menú 
	ldi ZL,LOW(Unlocked*2)
	rcall SRPintar
	ret

; Rutina para enviar imágenes a la pantalla

SRPintar:
	; Guarda los registros en la pila

	push regUsoGeneral
	push r20 
	push r21 
	
	; Asociamos los valores a los registros

	ldi r21, 64  
	ldi r20, 0

Return: 
	; Pintamos la pantalla izquierda

	rcall IZQ
	rcall Dibujar  
	
	; Pintamos la pantalla derecha

	rcall DER  
	rcall Dibujar  
	
	; Incrementamos el valor del registro asociado al eje vertical
	; y comparamos, si superamos el numero de páginas se empieza de 0

	inc r20   
	cpi r20,8
	brne Return 
	pop r21   
	pop r20 
	pop regUsoGeneral
	ret

; Configura la GLCD para empezar a escribir en la pantalla izquierda

IZQ:   

	; Guardamos los valores en la pila
	push regUsoGeneral
	push r20 

	rcall SRPantallaIzquierdaOn
	ldi regUsoGeneral,0b01000000 
	out PORTC, regUsoGeneral
	rcall SRModoConfig	
	ldi regUsoGeneral,0b10111000 
	or regUsoGeneral,r20 
	out PORTC, regUsoGeneral
	rcall SRModoConfig
	
	; Recuperamos los valores de la pila
	pop r20    
	pop regUsoGeneral
	ret


DER:   
	; Guardamos los valores en la pila

	push regUsoGeneral
	push r20 
	rcall SRPantallaDerechaOn
	ldi regUsoGeneral,0b01000000 
	out PORTC, regUsoGeneral
	rcall SRModoConfig
	ldi regUsoGeneral,0b10111000 
	or regUsoGeneral,r20 
	out PORTC, regUsoGeneral;R20 indicará la página a dibujar 
	rcall SRModoConfig
	pop r20  ;Se recupera los valores de los registros 
	pop regUsoGeneral
	ret

Dibujar:;Subrutina de apoyo para dibujar imagenes, una vez ajustado las direcciones y la    ;pantalla activada se procede a rellenar la página de los 64 pixeles. 
	push r0 ;Se guarda los valores de los registros en la pila 
	push r21 
ReDibujar: 
	lpm;Carga a R0 el valor de la dirección apuntada por el puntero. 
	out PORTC, r0 
	rcall Envio  ;Y se muestra por pantalla 
	adiw ZL,1  ;Se aumenta la dirección donde apunta el puntero 
	dec r21    
	brne ReDibujar  ;Cuando se haya dibujado 64 direcciones Y se terminará el bucle 
	pop r21 ;Se recupera los valores de los registros 
	pop r0 
	ret



SRPantallaIzquierdaOn:

	cbi PORTB,0;
	sbi PORTB,1
	ret

SRPantallaDerechaOn:

	sbi PORTB,0
	cbi PORTB,1
	ret

SRModoConfig:

	cbi PORTA,2
	cbi PORTA,3
	sbi PORTD,6
	rcall Retardo100us
	cbi PORTD,6
	rcall Retardo100us

	ret

Envio:

	sbi PORTA,2
	cbi PORTA,3
	sbi PORTD,6
	rcall Retardo100us
	cbi PORTD,6
	rcall Retardo100us
	ret

Leer:
	push regUsoGeneral

	ldi regUsoGeneral,0	;PC todo salida
	out DDRC, regUsoGeneral
	sbi PORTA,2
	sbi PORTA,3
	sbi PORTD,6
	rcall Retardo100us
	cbi PORTD,6
	rcall Retardo100us
	sbi PORTD,6
	rcall Retardo100us
	in r28, PINC
	cbi PORTD,6
	rcall Retardo100us
	ldi regUsoGeneral,255	;PC todo salida
	out DDRC, regUsoGeneral

	pop regUsoGeneral

	ret

RetardoLargo:
    ldi  r25, 41
    ldi  r26, 150
    ldi  r27, 128
L1: dec  r27
    brne L1
    dec  r26
    brne L1
    dec  r25
    brne L1

Retardo100us:
    ldi  r25, 26
L2: dec  r25
    brne L2
    ret


Retardo4ms:
    ldi  r25, 42
    ldi  r26, 142
L4: dec  r27
    brne L4
    dec  r25
    brne L4
    nop
	ret




